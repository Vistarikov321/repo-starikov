# Формула Кетле позволяет рассчитать индекс массы тела для мужчин. Она имеет следующий вид: I=m/h, где m — масса тела(кг), h — рост (м).
# Рассмотрим пример:
# 1) Необходимо вычислить индекс массы тела у мужчины со следующими данными: рост 193 сантиметра, вес 105 кг.
# 2) Первый шаг — необходимо возвести значение роста в метрах в квадрат: 1,93×1,93 = 3,72.
# 3) Коэффициент массы тела I составит: 105/3,72=28,23.

Height = float(input("Введите ваш рост в м. >>>"))
Weight = float(input("Введите ваш вес в кг. >>>"))
BMI = Weight / Height ** 2

print("Индекс массы тела равен", BMI, "это значит что у Вас")
if BMI <= 16:
    print('Выраженная нехватка веса, истощение организма')
elif BMI > 16 and BMI <= 19:
    print('Недостаток веса, имеется дефицит')
elif BMI > 19 and BMI <= 25:
    print("Нормальный ИМТ")
elif BMI > 25 and BMI <= 30:
    print("Имеется лишний вес")
elif BMI > 30 and BMI <= 35:
    print("Ожирение 1 типа")
elif BMI > 35 and BMI <= 40:
    print("Ожирение 2 типа")
else:
    print("Ожирение 3 типа")

